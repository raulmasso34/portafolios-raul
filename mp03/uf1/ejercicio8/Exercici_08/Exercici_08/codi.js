// Función para realizar el ordenamiento por inserción
function insertionSort(arr) {
  const n = arr.length;

  // Itera sobre el array desde el segundo elemento hasta el último
  for (let i = 1; i < n; i++) {
    // Guarda el valor del elemento actual en la variable key
    const key = arr[i];

    // Inicia un bucle while para comparar key con los elementos anteriores
    let j = i - 1;
    while (j >= 0 && (parseInt(arr[j].stat, 10) > parseInt(key.stat, 10) || (parseInt(arr[j].stat, 10) === parseInt(key.stat, 10) && arr[j].name.localeCompare(key.name) > 0))) {
      // Desplaza los elementos mayores que key hacia la derecha
      arr[j + 1] = arr[j];
      j = j - 1;
    }

    // Coloca key en la posición correcta en el array ordenado
    arr[j + 1] = key;
  }

  // Devuelve el array ordenado
  return arr;
}


