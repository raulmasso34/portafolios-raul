// Función para desordenar el array de objetos utilizando Fisher-Yates Shuffle
function mixIt(arr) {
  const n = arr.length;

  for (let i = n - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    // Intercambia arr[i] y arr[j]
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }

  return arr;
}

