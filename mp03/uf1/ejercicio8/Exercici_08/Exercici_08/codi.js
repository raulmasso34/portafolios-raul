// Función para ordenar el array de objetos utilizando el algoritmo Quick Sort
function quickSort(arr) {
  // Caso base: si el array tiene 1 elemento o está vacío, ya está ordenado
  if (arr.length <= 1) {
    return arr;
  }

  // Selecciona el primer elemento como pivote
  const pivot = arr[0];
  const left = [];
  const right = [];

  // Divide el array en dos partes: elementos menores que el pivote (left) y mayores (right)
  for (let i = 1; i < arr.length; i++) {
    const elementStat = parseInt(arr[i].stat, 10);
    const pivotStat = parseInt(pivot.stat, 10);

    // Compara la estadística y, en caso de empate, compara los nombres alfabéticamente
    if (elementStat < pivotStat || (elementStat === pivotStat && arr[i].name < pivot.name)) {
      left.push(arr[i]);
    } else {
      right.push(arr[i]);
    }
  }

  // Combina recursivamente las partes ordenadas y el pivote
  return quickSort(left).concat(pivot, quickSort(right));
}

